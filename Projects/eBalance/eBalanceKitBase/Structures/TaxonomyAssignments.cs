        private TaxonomyAssignments(){
            int id = 1;
            foreach(var key in _taxonomyToPosition.Keys){
                _taxonomyToId[key] = id++;
            }
        }
        private static TaxonomyAssignments instance = new TaxonomyAssignments();
        public static TaxonomyAssignments Instance { get { return instance; } }

        private Dictionary<string, int> _taxonomyToId = new Dictionary<string, int>();

        public Boolean HasId(string key) {
            return _taxonomyToId.ContainsKey(key) || _taxonomyToId.ContainsKey("de-gcd_" + key) || _taxonomyToId.ContainsKey("de-gaap-ci_" + key);
        }

        public int GetId(string key) {
            if (_taxonomyToId.ContainsKey(key)) return _taxonomyToId[key];
            if (_taxonomyToId.ContainsKey("de-gcd_" + key)) return _taxonomyToId["de-gcd_" + key];
            if (_taxonomyToId.ContainsKey("de-gaap-ci_" + key)) return _taxonomyToId["de-gaap-ci_" + key];
            
            throw new Exception("Konnte key " + key + " nicht finden in der Taxonomie.");
        }

        public void InsertIntoTaxonomyTable(IDatabase conn) {
            try {
                conn.BeginTransaction();
                conn.ClearTable("taxonomy_ids");
                var valueList = new List<DbColumnValues>();
                foreach (var keyValue in _taxonomyToId) {
                    DbColumnValues values = new DbColumnValues();
                    values["id"] = keyValue.Value;
                    values["taxonomy_id"] = keyValue.Key;
                    values["number"] = TaxonomyToPosition[keyValue.Key];
                    valueList.Add(values);                    
                }
                conn.InsertInto("taxonomy_ids", valueList);
                conn.CommitTransaction();
            } catch (Exception ex) {
                conn.RollbackTransaction();
                throw ex;
            }
        }

        public Dictionary<string, int> TaxonomyToId {
            get { return _taxonomyToId; }
            private set { _taxonomyToId = value; }
